## Frequently Asked Questions

### Can OCD cause addiction?

OCD does not directly cause addiction, but it significantly increases the risk. People with OCD often use alcohol, cannabis, or other substances to manage the intense anxiety that obsessions create. Over time, this self-medication pattern can develop into a full substance use disorder. The two conditions frequently co-occur and reinforce each other.

### How do I know if my loved one has OCD and addiction?

Look for a pattern of intrusive, unwanted thoughts alongside repetitive behaviors or rituals — combined with substance use that the person describes as a way to "turn off" their brain. If they have been through addiction treatment multiple times without lasting success, underlying OCD may be part of why. A dual-diagnosis assessment by a qualified clinician is the definitive way to know.

### What is the best treatment for OCD and addiction together?

The most effective approach treats both conditions simultaneously through a dual-diagnosis program. This typically includes Exposure and Response Prevention (ERP) therapy for the OCD, evidence-based addiction treatment, medication evaluation, and a relapse prevention plan that addresses both OCD triggers and substance use triggers. Longer treatment durations — 90 days or more — produce significantly better outcomes.

### Should I do an intervention if my loved one has OCD?

Yes — but the approach matters. OCD can intensify shame and self-criticism, so intervention conversations need to be structured carefully to avoid triggering defensive responses rooted in OCD anxiety. Working with a professional interventionist who understands dual diagnosis helps ensure the conversation is both effective and compassionate.

### Can someone recover from both OCD and addiction?

Yes. Both conditions are treatable, and people recover from this combination regularly. Recovery typically takes longer and requires more specialized care than single-diagnosis treatment, but with the right support — including ERP therapy, appropriate medication, and a strong recovery community — lasting recovery is absolutely achievable.

### Is OCD considered a dual diagnosis with addiction?

Yes. When OCD and a substance use disorder occur together, this is classified as a dual diagnosis (also called a co-occurring disorder). Dual-diagnosis treatment is designed to address both conditions at the same time, rather than treating one first and hoping the other resolves — an approach that rarely works when OCD is part of the picture.$body$
